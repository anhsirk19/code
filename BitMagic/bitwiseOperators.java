package BitMagic;


public class bitwiseOperators {
    public static void main(String[] args) {
        int a = 3;
        int b = 6;
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~3);
        System.out.println(3 << 2);
        System.out.println(8 >> 2);
        System.out.println(-1 << 2);
    }
}
